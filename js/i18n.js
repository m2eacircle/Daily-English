function t(key, vars) {
  var lang = getLang();
  var str  = (I18N[lang] && I18N[lang][key]) || (I18N['en'][key]) || key;
  if (vars) {
    Object.keys(vars).forEach(function(k) {
      str = str.replace(new RegExp('\\{' + k + '\\}', 'g'), vars[k]);
    });
  }
  return str;
}
